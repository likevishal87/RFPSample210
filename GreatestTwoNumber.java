package com.bridgelabz.basic;

public class GreatestTwoNumber {

	public static void main(String[] args) {

		int num1 = 30, num2 = 20;
		if (num1 == num2)
			System.out.println("both are equal");
		else if (num1 > num2)
			System.out.println(num1 + " is greater");

		else
			System.out.println(num2 + " is greater");

	}

}
